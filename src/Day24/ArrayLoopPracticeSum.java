package Day24;

public class ArrayLoopPracticeSum {

	public static void main(String[] args) {
		 // print them out using the loop 
	      // get the sum of all numbers 
	      // get the max number 
	      
	
	    
	    int nums []= {1,4,6,8,9};
	    
	    int sum = 0 ; 
	    for (int i = 0; i < nums.length; i++) {
	      
	      sum = sum + nums[i] ; 
	      
	    }
	    System.out.println(sum);
	    
	    
	    

	}

}
